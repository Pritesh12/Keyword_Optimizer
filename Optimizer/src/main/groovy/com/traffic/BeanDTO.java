public class BeanDTO  {

    private Double meanAverageCpc;
    private Double meanAveragePosition;
    private Double meanClicks;
    private Double meanTotalCost;

    public Double getMeanAverageCpc() {
        return meanAverageCpc;
    }

    public void setMeanAverageCpc(Double meanAverageCpc) {
        this.meanAverageCpc = meanAverageCpc;
    }

    public Double getMeanAveragePosition() {
        return meanAveragePosition;
    }

    public void setMeanAveragePosition(Double meanAveragePosition) {
        this.meanAveragePosition = meanAveragePosition;
    }

    public Double getMeanClicks() {
        return meanClicks;
    }

    public void setMeanClicks(Double meanClicks) {
        this.meanClicks = meanClicks;
    }

    public Double getMeanTotalCost() {
        return meanTotalCost;
    }

    public void setMeanTotalCost(Double meanTotalCost) {
        this.meanTotalCost = meanTotalCost;
    }


}
